void SwitchSPITiming(SPI_TypeDef * SPIn, BOOLEAN ClockIdle, BOOLEAN ClockPhase)
{
    INT16U Reg = 0;
    
    SPIn->CR1 &= ~(1<<6);
    
    Reg = SPIn->CR1;
    
    if(ClockIdle)
    {//clock high when idle
        Reg |= 1<<1;
    }
    else
    {//clock low when idle
        Reg &= ~(1<<1);
    }
    
    if(ClockPhase)
    {//edge 2 capture
        Reg |= 1;
    }
    else
    {//edge 1 capture
        Reg &= ~(1);
    }
    SPIn->CR1 = Reg;
    
    SPIn->CR1 |= (1<<6);
}

